namespace TreeMage.Core.Drawing.Styles
{
    /// <summary>
    /// 枝の装飾の種類を表します。
    /// </summary>
    public enum BranchDecorationType
    {
        /// <summary>
        /// 塗られた円を表します。
        /// </summary>
        ClosedCircle,

        /// <summary>
        /// 塗られていない円を表します。
        /// </summary>
        OpenCircle,

        /// <summary>
        /// 塗られた矩形を表します。
        /// </summary>
        ClosedRectangle,

        /// <summary>
        /// 塗られていない矩形を表します。
        /// </summary>
        OpenedRectangle,
    }
}
